package implicit;

public class Program 
{              // Implicit casting
	//Converting lower data type information into Higher
	//size of data increased hence it is called as WIDENING Casting
	
     public static void main(String[] args) 
     {
		int a = 75;
		System.out.println(a);           // int -----> double
    	 
		//casting-Implicit - Direct -->Automatically
		double b = a;
		System.out.println(b); 
    	 
	}
}
