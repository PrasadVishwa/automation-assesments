package explicit;

public class Program1 // EXplicit Casting
{
	//Converting higher data type information into lower
	
	//Size of data reduced hence it is called as Narrowing Casting
	
	
	
public static void main(String[] args) 
{
	int a = 20;
	short b = (short)a;
	System.out.println(b);
	
	double x = 20.35;
	System.out.println(x);
	
	int y = (int)x;
	System.out.println(y);
	
//	       =(int)double
//	    	=int x
//	    int y = int x	
//	
	
}
}
